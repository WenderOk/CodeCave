friends = int(input("Введите количество друзей: "))
apples = int(input("Введите количество яблок: "))
print("Сколько целых яблок достанется каждому другу: ",apples//friends)
print("Сколько яблок останется не разделенными: ",apples%friends)
print("По сколько получит каждый из друзей, если яблоки начнут делить: ",apples/friends)